    public EmojiPickerViewModel()
    {
        this.WhenAnyValue(x => x.SelectedCategoryIndex, x => x.SearchQuery)
            .Subscribe(_ =>
            {
                this.RaisePropertyChanged(nameof(CurrentCategory));
                this.RaisePropertyChanged(nameof(DisplayedEmojis));
            });
    }

    public string SearchQuery
    {
        get;
        init => this.RaiseAndSetIfChanged(ref field, value);
    } = string.Empty;

    public int SelectedCategoryIndex
    {
        get;
        set => this.RaiseAndSetIfChanged(ref field, value);
    }

    public EmojiCategory CurrentCategory => Categories[SelectedCategoryIndex];

    public string[] DisplayedEmojis =>
        !string.IsNullOrWhiteSpace(SearchQuery)
            ? FilterBySearch()
            : Categories[SelectedCategoryIndex].Emojis;

    private string[] FilterBySearch()
    {
        var q = SearchQuery.Trim();
        if (string.IsNullOrEmpty(q))
        {
            return [];
        }

        return Categories.Skip(1)
            .SelectMany(c => c.Emojis)
            .Where(e => e.Contains(q))
            .Distinct()
            .ToArray();
    }
